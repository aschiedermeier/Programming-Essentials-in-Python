# 5.1.9.8 String methods
# join() method

# Demonstrating the join() method
print(",".join(["omicron", "pi", "rho"]))

# Demonstrating the lower() method
print("SiGmA=60".lower())

# Demonstrating the lstrip() method
print("[" + " tau ".lstrip() + "]")

# Demonstrating the lstrip() method
print("[" + " tau ".lstrip() + "]")

print("www.cisco.com".lstrip("w.")) # takes away all leading "w" and then leading "."

print("pythoninstitute.org".lstrip(".org")) # takes away nothing, as "org" is not leading

